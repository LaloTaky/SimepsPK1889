using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace SIMEPS.Modelo
{
    public class IndicadoresPND
    {
        /// <summary>
        /// ID del objetivo al que pertenece el indicador
        /// </summary>
        public int ID_OBJETIVO_M { get; set; }
        /// <summary>
        /// ID del indicador
        /// </summary>
        public int ID_INDICADOR_PND { get; set; }
        /// <summary>
        /// ID del indicador anterior
        /// </summary>
        public int? ID_INDICADOR_ANT { get; set; }
        /// <summary>
        /// Nombre del indicador
        /// </summary>
        public string NOMBRE { get; set; }
        /// <summary>
        /// Definicion del indicador
        /// </summary>
        public string DEFINICION { get; set; }
        /// <summary>
        /// unidad de medida del indicador
        /// </summary>
        public string UNIDAD_MEDIDA { get; set; }
        /// <summary>
        /// valor alcanzado por el indicador del objetivo
        /// </summary>
        public decimal VALOR_ALCANZADO { get; set; }
        /// <summary>
        /// Indica si tiene calificacion del claridad
        /// </summary>
        public bool CALIF_CLARIDAD { get; set; }
        /// <summary>
        /// Indica si tiene calificacion de relevancia
        /// </summary>
        public bool CALIF_RELEVANCIA { get; set; }
        /// <summary>
        /// Indica si tiene calificacion de monitoreabilidad
        /// </summary>
        public bool CALIF_MONITOREABILIDAD { get; set; }
        /// <summary>
        /// Indica si tiene calificacion de pertenencia
        /// </summary>
        public bool CALIF_PERTINENCIA { get; set; }
        /// <summary>
        /// Indica el ciclo al que pertenece el indicador
        /// </summary>
        public int CICLO { get; set; }
        /// <summary>
        /// Especifica la categoria de calidad a la que pertenece
        /// </summary>
        public string CATEGORIA_CALIDAD { get; set; }
        /// <summary>
        /// Muestra el enfoque que tiene el indicador
        /// </summary>
        public string ENFOQUE_INDICADOR { get; set; }
        /// <summary>
        /// Muestra el comentario que tiene el indicador
        /// </summary>
        public string COMENTARIO { get; set; }
    }
}